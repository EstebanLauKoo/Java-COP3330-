package backend;

/**
 *
 * @author kwhiting
 */
public class DateOfBirth
{
    private int month;
    private int day;
    private int year;

    /**
     * @return the month
     */
    public int getMonth()
    {
        return month;
    }

    /**
     * @param month the month to set
     */
    public void setMonth(int month)
    {
        this.month = month;
    }

    /**
     * @return the day
     */
    public int getDay()
    {
        return day;
    }

    /**
     * @param day the day to set
     */
    public void setDay(int day)
    {
        this.day = day;
    }

    /**
     * @return the year
     */
    public int getYear()
    {
        return year;
    }

    /**
     * @param year the year to set
     */
    public void setYear(int year)
    {
        this.year = year;
    }
}
